package info

import (
	"errors"
	"net"
)

// MyIP returns non-multicast/loopback IP found
func MyIP() (net.IP, error) {
	ifaces, err := net.Interfaces()
	if err != nil {
		return nil, err
	}
	for _, i := range ifaces {
		addrs, err := i.Addrs()
		if err != nil {
			continue
		}

		for _, addr := range addrs {
			var ip net.IP
			switch v := addr.(type) {
			case *net.IPNet:
				ip = v.IP
			case *net.IPAddr:
				ip = v.IP
			}
			if ip.IsLoopback() || ip.IsMulticast() {
				continue
			}
			return ip, nil
		}
	}
	return nil, errors.New("not-found")
}
